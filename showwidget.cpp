#include "showwidget.h"
#include <QLayout>
#include <QVector>

ShowWidget::ShowWidget(QWidget *parent) : QWidget(parent)
{
    polt_ = new QCustomPlot(this);
    plot_1_ = new QCustomPlot(this);
    plot_2_ = new QCustomPlot(this);

    QGridLayout *glay = new QGridLayout(this);
    glay->addWidget(polt_, 0, 0, 1, 1);
    glay->addWidget(plot_1_, 0, 1, 1, 1);
    glay->addWidget(plot_2_, 1, 0, 1, 1);
    testcase_1();
    testcase_2();
    testcase_3();
}

void ShowWidget::testcase_1()
{
    QVector<double> x1(20), y1(20);
    QVector<double> x2(100), y2(100);
    QVector<double> x3(20), y3(20);
    QVector<double> x4(20), y4(20);
    for (int i=0; i<x1.size(); ++i)
    {
      x1[i] = i/(double)(x1.size()-1)*10;
      y1[i] = qCos(x1[i]*0.8+qSin(x1[i]*0.16+1.0))*qSin(x1[i]*0.54)+1.4;
    }
    for (int i=0; i<x2.size(); ++i)
    {
      x2[i] = i/(double)(x2.size()-1)*10;
      y2[i] = qCos(x2[i]*0.85+qSin(x2[i]*0.165+1.1))*qSin(x2[i]*0.50)+1.7;
    }
    for (int i=0; i<x3.size(); ++i)
    {
      x3[i] = i/(double)(x3.size()-1)*10;
      y3[i] = 0.05+3*(0.5+qCos(x3[i]*x3[i]*0.2+2)*0.5)/(double)(x3[i]+0.7)+qrand()/(double)RAND_MAX*0.01;
    }
    for (int i=0; i<x4.size(); ++i)
    {
      x4[i] = x3[i];
      y4[i] = (0.5-y3[i])+((x4[i]-2)*(x4[i]-2)*0.02);
    }

    QCPGraph *graph1 = polt_->addGraph();
    graph1->setData(x1, y1);
    //设置 graph1 的散点样式
    graph1->setScatterStyle(QCPScatterStyle(QCPScatterStyle::ssCircle, QPen(Qt::black, 1.5), QBrush(Qt::white), 9));
    graph1->setPen(QPen(QColor(120, 120, 120), 2));

    QCPGraph *graph2 = polt_->addGraph();
    graph2->setData(x2, y2);
    graph2->setPen(Qt::NoPen);
    graph2->setBrush(QColor(200, 200, 200, 20));
    //这行代码将 graph1 设置为 graph2 的填充图形。这意味着 graph2 将在 graph1 的下方填充颜色，形成一个填充区域
    graph2->setChannelFillGraph(graph1);

    QCPBars *bars1 = new QCPBars(polt_->xAxis, polt_->yAxis);
    bars1->setWidth(9/(double)x3.size());
    bars1->setData(x3, y3);
    bars1->setPen(Qt::NoPen);
    bars1->setBrush(QColor(10, 140, 70, 160));

    QCPBars *bars2 = new QCPBars(polt_->xAxis, polt_->yAxis);
    bars2->setWidth(9/(double)x4.size());
    bars2->setData(x4, y4);
    bars2->setPen(Qt::NoPen);
    bars2->setBrush(QColor(10, 100, 50, 70));
    bars2->moveAbove(bars1);

    //图层功能
    polt_->addLayer("abovemain", polt_->layer("main"), QCustomPlot::limAbove);
    polt_->addLayer("belowmain", polt_->layer("main"), QCustomPlot::limBelow);
    graph1->setLayer("abovemain");
    polt_->xAxis->grid()->setLayer("belowmain");
    polt_->yAxis->grid()->setLayer("belowmain");


    //设置坐标轴的主线笔:
    polt_->xAxis->setBasePen(QPen(Qt::white, 1));
    polt_->yAxis->setBasePen(QPen(Qt::white, 1));
    //设置坐标轴的刻度线笔:
    polt_->xAxis->setTickPen(QPen(Qt::white, 1));
    polt_->yAxis->setTickPen(QPen(Qt::white, 1));
    //设置坐标轴的子刻度线笔:
    polt_->xAxis->setSubTickPen(QPen(Qt::white, 1));
    polt_->yAxis->setSubTickPen(QPen(Qt::white, 1));
    //设置刻度标签颜色:
    polt_->xAxis->setTickLabelColor(Qt::white);
    polt_->yAxis->setTickLabelColor(Qt::white);
    //设置网格线的笔:
    polt_->xAxis->grid()->setPen(QPen(QColor(140, 140, 140), 1, Qt::DotLine));
    polt_->yAxis->grid()->setPen(QPen(QColor(140, 140, 140), 1, Qt::DotLine));
    polt_->xAxis->grid()->setSubGridPen(QPen(QColor(80, 80, 80), 1, Qt::DotLine));
    polt_->yAxis->grid()->setSubGridPen(QPen(QColor(80, 80, 80), 1, Qt::DotLine));
    //设置子网格可见性:
    polt_->xAxis->grid()->setSubGridVisible(false);
    polt_->yAxis->grid()->setSubGridVisible(true);
    //设置零线的笔:
    polt_->xAxis->grid()->setZeroLinePen(Qt::NoPen);
    polt_->yAxis->grid()->setZeroLinePen(Qt::NoPen);
    //设置坐标轴的箭头样式:
    polt_->xAxis->setUpperEnding(QCPLineEnding::esSpikeArrow);
    polt_->yAxis->setUpperEnding(QCPLineEnding::esFlatArrow);

    QLinearGradient plotGradient;
    plotGradient.setStart(0, 0);
    plotGradient.setFinalStop(0, 350);
    plotGradient.setColorAt(0, QColor(80, 80, 80));
    plotGradient.setColorAt(1, QColor(50, 50, 50));
    polt_->setBackground(plotGradient);

    QLinearGradient axisRectGradient;
    axisRectGradient.setStart(0, 0);
    axisRectGradient.setFinalStop(0, 350);
    axisRectGradient.setColorAt(0, QColor(80, 80, 80));
    axisRectGradient.setColorAt(1, QColor(30, 30, 30));
    polt_->axisRect()->setBackground(axisRectGradient);

    polt_->rescaleAxes();
    polt_->yAxis->setRange(0, 2);
}

void ShowWidget::testcase_2()
{
    QCustomPlot *customPlot = plot_1_;
    // set dark background gradient:
    QLinearGradient gradient(0, 0, 0, 400);
    gradient.setColorAt(0, QColor(90, 90, 90));
    gradient.setColorAt(0.38, QColor(105, 105, 105));
    gradient.setColorAt(1, QColor(70, 70, 70));
    customPlot->setBackground(QBrush(gradient));

    // create empty bar chart objects:
    QCPBars *regen = new QCPBars(customPlot->xAxis, customPlot->yAxis);
    QCPBars *nuclear = new QCPBars(customPlot->xAxis, customPlot->yAxis);
    QCPBars *fossil = new QCPBars(customPlot->xAxis, customPlot->yAxis);
    regen->setAntialiased(false); // gives more crisp, pixel aligned bar borders
    nuclear->setAntialiased(false);
    fossil->setAntialiased(false);
    regen->setStackingGap(1);
    nuclear->setStackingGap(1);
    fossil->setStackingGap(1);
    // set names and colors:
    fossil->setName("Fossil fuels");
    fossil->setPen(QPen(QColor(111, 9, 176).lighter(170)));
    fossil->setBrush(QColor(111, 9, 176));
    nuclear->setName("Nuclear");
    nuclear->setPen(QPen(QColor(250, 170, 20).lighter(150)));
    nuclear->setBrush(QColor(250, 170, 20));
    regen->setName("Regenerative");
    regen->setPen(QPen(QColor(0, 168, 140).lighter(130)));
    regen->setBrush(QColor(0, 168, 140));
    // stack bars on top of each other:
    nuclear->moveAbove(fossil);
    regen->moveAbove(nuclear);

    // prepare x axis with country labels:
    QVector<double> ticks;
    QVector<QString> labels;
    ticks << 1 << 2 << 3 << 4 << 5 << 6 << 7;
    labels << "USA" << "Japan" << "Germany" << "France" << "UK" << "Italy" << "Canada";
    QSharedPointer<QCPAxisTickerText> textTicker(new QCPAxisTickerText);
    textTicker->addTicks(ticks, labels);
    customPlot->xAxis->setTicker(textTicker);
    customPlot->xAxis->setTickLabelRotation(60);
    customPlot->xAxis->setSubTicks(false);
    customPlot->xAxis->setTickLength(0, 4);
    customPlot->xAxis->setRange(0, 8);
    customPlot->xAxis->setBasePen(QPen(Qt::white));
    customPlot->xAxis->setTickPen(QPen(Qt::white));
    customPlot->xAxis->grid()->setVisible(true);
    customPlot->xAxis->grid()->setPen(QPen(QColor(130, 130, 130), 0, Qt::DotLine));
    customPlot->xAxis->setTickLabelColor(Qt::white);
    customPlot->xAxis->setLabelColor(Qt::white);

    // prepare y axis:
    customPlot->yAxis->setRange(0, 12.1);
    customPlot->yAxis->setPadding(5); // a bit more space to the left border
    customPlot->yAxis->setLabel("Power Consumption in\nKilowatts per Capita (2007)");
    customPlot->yAxis->setBasePen(QPen(Qt::white));
    customPlot->yAxis->setTickPen(QPen(Qt::white));
    customPlot->yAxis->setSubTickPen(QPen(Qt::white));
    customPlot->yAxis->grid()->setSubGridVisible(true);
    customPlot->yAxis->setTickLabelColor(Qt::white);
    customPlot->yAxis->setLabelColor(Qt::white);
    customPlot->yAxis->grid()->setPen(QPen(QColor(130, 130, 130), 0, Qt::SolidLine));
    customPlot->yAxis->grid()->setSubGridPen(QPen(QColor(130, 130, 130), 0, Qt::DotLine));

    // Add data:
    QVector<double> fossilData, nuclearData, regenData;
    fossilData  << 0.86*10.5 << 0.83*5.5 << 0.84*5.5 << 0.52*5.8 << 0.89*5.2 << 0.90*4.2 << 0.67*11.2;
    nuclearData << 0.08*10.5 << 0.12*5.5 << 0.12*5.5 << 0.40*5.8 << 0.09*5.2 << 0.00*4.2 << 0.07*11.2;
    regenData   << 0.06*10.5 << 0.05*5.5 << 0.04*5.5 << 0.06*5.8 << 0.02*5.2 << 0.07*4.2 << 0.25*11.2;
    fossil->setData(ticks, fossilData);
    nuclear->setData(ticks, nuclearData);
    regen->setData(ticks, regenData);

    // setup legend:
    customPlot->legend->setVisible(true);
    customPlot->axisRect()->insetLayout()->setInsetAlignment(0, Qt::AlignTop|Qt::AlignHCenter);
    customPlot->legend->setBrush(QColor(255, 255, 255, 100));
    customPlot->legend->setBorderPen(Qt::NoPen);
    QFont legendFont = font();
    legendFont.setPointSize(10);
    customPlot->legend->setFont(legendFont);
    //许用户通过拖动和缩放来与图表进行交互
    customPlot->setInteractions(QCP::iRangeDrag | QCP::iRangeZoom);
}

